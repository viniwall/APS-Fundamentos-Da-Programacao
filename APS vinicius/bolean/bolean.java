package bolean;
import java.util.Scanner;

public class bolean {
    public static void main(String[] args) {
        // declaração de s como scanner
        Scanner s = new Scanner(System.in);

        // declaração dos valores booleanos
        boolean valor1 = true;
        boolean valor2 = false;
        // impressão dos resultados
        System.out.println("valor booleano 1: "+valor1);
        System.out.println("valor booleano 2: "+valor2);
        s.close();
    }
}